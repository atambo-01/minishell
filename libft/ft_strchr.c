#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	if (!s || !*s)
		return (NULL);
	while (*s)
	{
		if (*s == (char)c)
			return ((char *)s);
		s++;
	}
	if (c == 0)
		return ((char *)s);
	return (NULL);
}
