#include "./libft.h"

int	ft_strcpy(char *dst, char *src)
{
	int	i;

	if (!src || !dst)
		return (0);
	i = 0;
	while (src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = 0;
	return (i);
}
